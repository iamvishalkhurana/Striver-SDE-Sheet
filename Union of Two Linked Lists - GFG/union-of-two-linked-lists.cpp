class Solution
{
    public:
    struct Node* makeUnion(struct Node* head1, struct Node* head2)
    {
        map<int,int> m1;
        while(head1){
            m1[head1->data]++;
            head1=head1->next;
        }
        while(head2){
            m1[head2->data]++;
            head2=head2->next;
        }
        
        auto it1=m1.begin();
        struct Node *head=NULL, *last=NULL;
        while(it1!=m1.end()){
                struct Node* temp=new struct Node(it1->first);
                if(head==NULL){
                    head=temp;
                    last=temp;
                }
                else{
                    last->next=temp;
                    last=temp;
                }
                it1++;
            }
        return head;
        
    }
};


